#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}
#include <unistd.h>
int main(int ac, char **av)
{
	write(1,av[1], ft_strlen(av[1]));
}